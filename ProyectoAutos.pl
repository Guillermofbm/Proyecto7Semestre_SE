% Reglas para obtener información de un carro específico
marca(Marca, Carro) :- carro(Carro, _, _, _, _, _), Marca = Carro.
anio(Anio, Carro) :- carro(Carro, Anio, _, _, _, _).
combustible(Combustible, Carro) :- carro(Carro, _, Combustible, _, _, _).
color(Color, Carro) :- carro(Carro, _, _, Color, _, _).
motor(CC, Potencia, Combustible, Carro) :-
    carro(Carro, _, _, _, motor(CC, Potencia, Combustible), _).
consumo(Consumo, Carro) :- carro(Carro, _, _, _, _, consumo(Consumo)).

% Regla para obtener una lista de carros con un consumo específico
carros_con_consumo(Consumo, ListaCarros) :- findall(Carro, carro(Carro, _, _, _, _, consumo(Consumo)), ListaCarros).
